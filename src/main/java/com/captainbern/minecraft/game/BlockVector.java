package com.captainbern.minecraft.game;

public class BlockVector {

    private int x;
    private int y;
    private int z;

    public BlockVector(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

    public int getZ() {
        return this.x;
    }
}
